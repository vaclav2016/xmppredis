	char access_token[DEFAUL_STR_SIZE];
	char need_access_token;

	char lastMessageId[DEFAUL_STR_SIZE];

	char inboundQueue[DEFAUL_STR_SIZE];
	char outboundQueue[DEFAUL_STR_SIZE];
